package com.biz.hello;

public class VarBoolean_01 
{
	public static void main(String[] args) 
	{
		System.out.println(3 == 3);
		System.out.println(3 > 3);
		System.out.println(3 < 3);
		System.out.println(3 <= 3);
		System.out.println(3 >= 3);
		System.out.println(3 != 3);
		
		/* boolean 대수
		 * A,B가 각각 true 이거나 false 경우의 값만 가질수 있는 경우
		 * 이 값을 boolean 이라고 표현한다. 
		 */
	}
	
}
