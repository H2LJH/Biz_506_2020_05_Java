package com.biz.hello;

public class VarBoolean_02 
{
	public static void main(String[] agrs)
	{
		int num1 = 3;
		int num2 = 2;

		if((num1+num2) % 2 == 0) System.out.println("짝수");
		else System.out.println("짝수 아님");

		if((num1-num2) % 2 == 0) System.out.println("짝수");
		else System.out.println("짝수 아님");
			
		if((num1*num2) % 2 == 0) System.out.println("짝수");
		else System.out.println("짝수 아님");

		if((num1/num2) % 2 == 0) System.out.println("짝수");
		else System.out.println("짝수 아님");

	}
}
