package com.biz.hello;

public class Var_02 
{
	public static void main(String[] args)
	{
		// main() method scope
		int num1 = 30;
		int num2 = 40;
		
		/*
		 	변수 선언규칙
		 	1. 변수명 : (int ex || int _ex)
		 	      변수명은 목적에 맞게 설정하는게 좋다. ex : 총합 변수선언 (int total)
		 	      두 단어 이상 연결하여 변수명을 작성할때는 (ex_ex || exEx) 패턴이 일반적
		 	   snake case : ex_ex
		 	   camel case : exEx
		    2. 변수 선언 불가 항목
		       keyword는 변수로 선언할수 없다.
		 	   
		 */
		
	}
}
