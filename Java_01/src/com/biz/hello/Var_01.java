package com.biz.hello;

// var = variable, value, 변수

public class Var_01 
{
	public static void main(String[] args)
	{
		// int형식의 num1제목의 변수를 선언후 숫자를 할당
		int num1 = 30;
		int num2 = 40;
		System.out.println(num1 + num2);
		System.out.println(num2 - num1);
		System.out.println(num1 * num2);
		System.out.println(num2 / num1);
		
	}
}
