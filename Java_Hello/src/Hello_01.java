public class Hello_01 
{
	public static void main(String[] args)
	{
		//큰따옴표(" ")로 묶인 내용은 문자열 리터럴 이라고 한다. 
		System.out.println("Repubilc of Korea");
		System.out.println("30+40");
		System.out.println("30*40");
		
		System.out.println("Repubilc" + "of" + "Korea"); //문자열 연산이 가능하다.
		System.out.println("Repubilc" + " of " + "Korea"); //" "은 빈칸(공백)도 인식한다.
	}
}

